import { NextRequest, NextResponse } from "next/server";
import { db } from "@/lib/db";
import { auth } from "@/lib/auth";

export async function GET(req: NextRequest, { params }: { params: Promise<{ id: string }> }) {
  const session = await auth();
  if (!session?.user) return NextResponse.json({ error: "Unauthorized" }, { status: 401 });

  const { id } = await params;
  const stakeholders = await db.stakeholder.findMany({ where: { projectId: id } });
  return NextResponse.json({ data: stakeholders });
}

export async function POST(req: NextRequest, { params }: { params: Promise<{ id: string }> }) {
  const session = await auth();
  if (!session?.user) return NextResponse.json({ error: "Unauthorized" }, { status: 401 });

  const { id } = await params;
  const body = await req.json();
  const stakeholder = await db.stakeholder.create({ data: { ...body, projectId: id } });
  return NextResponse.json({ data: stakeholder }, { status: 201 });
}
